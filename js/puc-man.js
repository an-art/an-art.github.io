function Tile(x, y, type) {
    this.x = x;
    this.y = y;
    this.type = type;

    this.dX = -1;
    this.dY = -1;
    this.moving = false;
    this.speed = 0.5;
    this.intact = true;
};

Tile.prototype.update = function() {

	if (!this.intact)
		return;

    if (this.moving) {
        this.x = lerp(this.x, this.dX, this.speed);
        this.y = lerp(this.y, this.dY, this.speed);
        if (Math.abs(this.x - this.dX) < 0.1 && Math.abs(this.y - this.dY) < 0.1) {
            this.x = this.dX;
            this.y = this.dY;
            this.moving = false;
        }
    }

   if (this.type == "PUCMAN") {
    	
	    let dTileX = Math.floor(this.x);
	    let dTileY = Math.floor(this.y);


	    let dTile = getTile(dTileX, dTileY);

	    if (dTile.intact) {
	    	switch(dTile.type) {
		    	case 'RICE':
		    		score++;
		    		dTile.intact = false;
		    		break;
		    	case 'SUSHI':
		    		score+=10;
		    		dTile.intact = false;
		    		break;
		    	case 'SUMO':
		    		endGame(false);
		    		break;
	    	}
	    } 
        if (score == endScore) endGame(true);
	}
	   else if (this.type == 'SUMO') {
            if (this.moving) return;

            let possibleMoves = [
                getTile(this.x - 1, this.y),
                getTile(this.x + 1, this.y),
                getTile(this.x, this.y - 1),
                getTile(this.x, this.y + 1),
            ];

            possibleMoves.sort(function (a, b) {
                let aD = dist(a.x, a.y, pucman.x, pucman.y);
                let bD = dist(b.x, b.y, pucman.x, pucman.y);
                return aD - bD;
            });
            for (let i = 0; i < possibleMoves.length; i++) {
                if (this.move(possibleMoves[i].x, possibleMoves[i].y, false))
                    break;
            }
		}
};

Tile.prototype.move = function(x, y, relative) {
    let dY;
    let dX;

    if (relative) {
        dY = this.y + y;
        dX = this.x + x;
    } else {
        dY = y;
        dX = x;
    }
        
    if (this.moving) {
        return false;
    }

    let destinationTile = getTile(dX, dY);
    let type = destinationTile.type;

    if ((type == 'BARRIER' && this.type != 'BARRIER') ||
    	(type == 'SUMO' && this.type == 'SUMO')) {
        return false;
    }

    this.moving = true;
    this.dX = dX;
    this.dY = dY;
    return true;
};

